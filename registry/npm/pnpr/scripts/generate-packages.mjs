// Adapted from pacquet/npm/pacquet/scripts/generate-packages.mjs, which was
// itself copied from Rome (https://github.com/rome/tools/blob/392d188a49/npm/rome/scripts/generate-packages.mjs).

import { resolve } from "node:path";
import { fileURLToPath } from "node:url";
import * as fs from "node:fs";

const BIN_NAME = "pnpr";
// Per-platform binary packages live under the `@pnpm` scope with a
// `pnpr.` name prefix, e.g. `@pnpm/pnpr.linux-x64`. The main wrapper
// package is `@pnpm/pnpr`.
const NATIVE_SCOPE = "@pnpm";
const NATIVE_NAME_PREFIX = "pnpr";
const REGISTRY_NPM_ROOT = resolve(fileURLToPath(import.meta.url), "../..");
const PACKAGES_ROOT = resolve(REGISTRY_NPM_ROOT, "..");
const REPO_ROOT = resolve(PACKAGES_ROOT, "../..");
const MANIFEST_PATH = resolve(REGISTRY_NPM_ROOT, "package.json");

const rootManifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, "utf-8"));

function nativePackageName(platform, arch) {
  return `${NATIVE_SCOPE}/${NATIVE_NAME_PREFIX}.${platform}-${arch}`;
}

function generateNativePackage(platform, arch) {
  const packageName = nativePackageName(platform, arch);
  const packageRoot = resolve(PACKAGES_ROOT, `${BIN_NAME}-${platform}-${arch}`);

  // Remove the directory just in case it already exists (it's autogenerated
  // so there shouldn't be anything important there anyway)
  fs.rmSync(packageRoot, { recursive: true, force: true });

  console.log(`Create directory ${packageRoot}`);
  fs.mkdirSync(packageRoot);

  const { version } = rootManifest;

  // publishConfig.executableFiles tells pnpm pack to keep mode 0755
  // on the binary in the published tarball. Without it, pack normalizes
  // every non-bin file to 0644 and the JS shim's spawnSync fails with
  // EACCES on install.
  const ext = platform === "win32" ? ".exe" : "";
  const manifest = JSON.stringify({
    name: packageName,
    version,
    os: [platform],
    cpu: [arch],
    repository: {
      type: "git",
      url: "https://github.com/pnpm/pnpm",
    },
    publishConfig: {
      executableFiles: [`./${BIN_NAME}${ext}`],
    },
  }, null, 2);

  const manifestPath = resolve(packageRoot, "package.json");
  console.log(`Create manifest ${manifestPath}`);
  fs.writeFileSync(manifestPath, manifest);

  const binarySource = resolve(REPO_ROOT, `${BIN_NAME}-${platform}-${arch}${ext}`);
  const binaryTarget = resolve(packageRoot, `${BIN_NAME}${ext}`);

  console.log(`Copy binary ${binaryTarget}`);
  fs.copyFileSync(binarySource, binaryTarget);
  fs.chmodSync(binaryTarget, 0o755);
}

function writeManifest() {
  const manifestPath = resolve(PACKAGES_ROOT, BIN_NAME, "package.json");

  const manifestData = JSON.parse(fs.readFileSync(manifestPath, "utf-8"));

  const nativePackages = PLATFORMS.flatMap((platform) =>
    ARCHITECTURES.map((arch) => [nativePackageName(platform, arch), rootManifest.version])
  );

  manifestData["version"] = rootManifest.version;
  manifestData["optionalDependencies"] = Object.fromEntries(nativePackages);

  console.log(`Update manifest ${manifestPath}`);
  const content = JSON.stringify(manifestData);
  fs.writeFileSync(manifestPath, content);
}

const PLATFORMS = ["win32", "darwin", "linux"];
const ARCHITECTURES = ["x64", "arm64"];

for (const platform of PLATFORMS) {
  for (const arch of ARCHITECTURES) {
    generateNativePackage(platform, arch);
  }
}

writeManifest();
